package LiveCoding;


public class verstaendnisfrage {

	public static void main(String[] args) {
		for(int i=1;i<10;i++){
			System.out.println("i = "+i);
		}
		
		for(int i=10;i>=0;i--){
			System.out.println("i = "+i);
		}
		
		for(int i=20;i<=90;){
			System.out.println("i = "+i);
			i=i+5;
		}

	}
	


}
